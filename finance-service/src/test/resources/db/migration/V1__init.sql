CREATE TABLE USERS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    EMAIL VARCHAR(100) NOT NULL,
    NAME VARCHAR(255),
    CONSTRAINT PK_USERS PRIMARY KEY (ID),
    CONSTRAINT uq_USERS_EMAIL UNIQUE (EMAIL)
);
-- End USERS table

CREATE TABLE TRANSACTIONS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    USER_ID BIGINT,
    AMOUNT NUMERIC(10,2),
    CATEGORY VARCHAR(100),
    TYPE VARCHAR(20), -- EXPENSE / INCOME
    DESCRIPTION TEXT,
    CREATED_TS TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT PK_TRANSACTIONS PRIMARY KEY (ID),
    CONSTRAINT FK_TRANSACTIONS_USERS FOREIGN KEY (USER_ID) REFERENCES USERS(ID) ON DELETE CASCADE

);

CREATE INDEX IDX_TRANSACTIONS_USER_ID ON TRANSACTIONS(USER_ID);

CREATE INDEX IDX_TRANSACTIONS_CREATED_TS ON TRANSACTIONS(CREATED_TS);

CREATE INDEX idx_TRANSACTIONS_USER_CREATED_TS ON TRANSACTIONS(USER_ID, CREATED_TS DESC);

-- End TRANSACTIONS table

CREATE TABLE BUDGETS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    USER_ID BIGINT,
    CATEGORY VARCHAR(100),
    MONTHLY_LIMIT NUMERIC(10,2),
    CONSTRAINT PK_BUDGETS PRIMARY KEY (ID),
    CONSTRAINT FK_BUDGETS_USERS FOREIGN KEY (USER_ID) REFERENCES USERS(ID) ON DELETE CASCADE
);

CREATE INDEX IDX_BUDGETS_USER_ID ON BUDGETS(USER_ID);
-- End BUDGETS table

CREATE TABLE INSIGHTS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    USER_ID BIGINT NOT NULL,
    QUERY TEXT,
    RESPONSE TEXT,
    CREATED_TS TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT PK_INSIGHTS PRIMARY KEY (ID),
    CONSTRAINT FK_INSIGHTS_USERS FOREIGN KEY (USER_ID) REFERENCES USERS(ID) ON DELETE CASCADE
);

CREATE INDEX IDX_INSIGHTS_USER_ID ON INSIGHTS(USER_ID);
CREATE INDEX IDX_INSIGHTS_CREATED_TS ON INSIGHTS(CREATED_TS DESC);
CREATE INDEX IDX_INSIGHTS_USER_CREATED_TS ON INSIGHTS(USER_ID, CREATED_TS DESC);
-- End INSIGHTS table